#include <CodeTool.hpp>

//-------------------------------------------------------------------------------------------------------
CodeTool::CodeTool()
	: ICodeTool(Tool::Configs{})
{
}

//-------------------------------------------------------------------------------------------------------
void CodeTool::onOpen()
{
	Tool::setWidget(this);
	Tool::setTitle("Code Tool");
}

//-------------------------------------------------------------------------------------------------------
void CodeTool::onUpdate(float dt)
{
}

//-------------------------------------------------------------------------------------------------------
void CodeTool::onClose()
{
}

//-------------------------------------------------------------------------------------------------------
void CodeTool::setText(std::string text)
{
	QPlainTextEdit::setPlainText(text.c_str());
}

//-------------------------------------------------------------------------------------------------------
std::string CodeTool::getText() const
{
	return qPrintable(QPlainTextEdit::toPlainText());
}
